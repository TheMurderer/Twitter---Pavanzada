private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
// TODO add your handling code here:

    ArrayList<Component> lista = new ArrayList<Component>();
    Usuarios aa = new Usuarios();

    for (int i = 0; i < jPanel8.getComponentCount(); i++) {
        aa = (Usuarios) jPanel8.getComponent(i);
        if (aa.statusCheckbox()) {
            if (!(userLoguin.getAmigos().contains(uja.buscarUsuarioCorreoE(aa.getEmail())))
                    && !(uja.buscarUsuarioCorreoE(aa.getEmail()).existeSolicitudAmigo(userLoguin))) {
                userLoguin.solicitudAmistad(uja.buscarUsuarioCorreoE(aa.getEmail()));                
                jPanel8.removeAll();
                jPanel8.updateUI();
                jLabel4.setText("Su petición de amistad ha sido enviada.");
            }

        }
    }
}//GEN-LAST:event_jButton4ActionPerformed

private void Button_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_BuscarActionPerformed
// TODO add your handling code here:
    Collection<Usuario> UsuariosRed = new LinkedList<Usuario>();
    jPanel8.removeAll();
    if (jTextArea2.getText().length() < 40) {
        UsuariosRed = uja.buscarUsuario(jTextArea2.getText().trim());
        List<Usuario> u = (List<Usuario>) UsuariosRed;
        List<Usuario> amigos = userLoguin.getAmigos();
        if (u.size() >= 1) {
            for (int i = 0; i < u.size(); i++) {             
                if (!amigos.contains(u.get(i)) && !u.get(i).existeSolicitudAmigo(userLoguin) && !userLoguin.equals(u.get(i))) {
                    Usuarios uses = new Usuarios();
                    uses.establecerCampos(u.get(i).getEmail(), u.get(i).getDescripcion(), u.get(i).getNombre());
                    jPanel8.add(uses);
                }
                
            }

        }
        jPanel8.updateUI();
        variableSesion.restart();
    } else {
        jLabel4.setText("Ha sobrepasado el maximo de carácteres permitido (40)");
    }
    t.start();
}//GEN-LAST:event_Button_BuscarActionPerformed

