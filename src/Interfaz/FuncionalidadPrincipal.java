    private void ElementosListAmigos() {
        jList2.removeAll();
        List<Usuario> lis = new ArrayList<Usuario>();
        lis = userLoguin.getAmigos();
        if (lis.size() >= 1) {
            DefaultListModel<String> modeloLista = new DefaultListModel<String>();
            for (int i = 0; i < lis.size(); i++) {
                modeloLista.addElement(lis.get(i).getNombre());
            }
            jList2.setModel(modeloLista);
            
        }
    }

    private void obtenerSolicitudesAmistad() {
        jPanel6.removeAll();
        List<Usuario> lis = new ArrayList<Usuario>();
        lis = userLoguin.getSolicitudesAmistad();
        if (lis.size() >= 1) {

            for (int i = 0; i < lis.size(); i++) {
                Usuarios usus = new Usuarios();
                usus.establecerCampos(lis.get(i).getEmail(), lis.get(i).getDescripcion(), lis.get(i).getNombre());
                jPanel6.add(usus);
            }
        }
    }

    private void listarTablon() {
        jPanel_TPublico.removeAll();
        PanelMensajesPrivado.removeAll();

        List<Mensaje> tablon = new LinkedList<Mensaje>();
        tablon = userLoguin.getMensajesRecibidos();
        for (int i = 0; i < tablon.size(); i++) {
            if (!(tablon.get(i) instanceof MensajePrivado)) {
                Mensajes e = new Mensajes();
                e.establecerCampos(tablon.get(i).getContenido(), tablon.get(i).getEmisor().getNombre(), tablon.get(i).getFecha().toString());

                jPanel_TPublico.add(e);
            } else {
                Mensajes e = new Mensajes();
                e.establecerCampos(tablon.get(i).getContenido(), tablon.get(i).getEmisor().getNombre(), tablon.get(i).getFecha().toString());

                PanelMensajesPrivado.add(e);
            }
        }
    }
    
  
